def partition(num, maxi):
    """Partitions num in all possible groups, eg. part(4)=(4),(1,1,1,1),(3,1),(2,2)
    and all permutations
    """
    if num == 0:
        return [[]]
    if maxi == 1:
        return [[1]*num]

    parts = []
    for i in range(1, maxi + 1):
        parts += [[i] + p for p in partition(num - i, min(i, num - i))]
    return parts


def unique_permutations(elements):
    if len(elements) == 1:
        yield (elements[0],)
    else:
        unique_elements = set(elements)
        for first_element in unique_elements:
            remaining_elements = list(elements)
            remaining_elements.remove(first_element)
            for sub_permutation in unique_permutations(remaining_elements):
                yield (first_element,) + sub_permutation

def distribution(num, slots):
    p = [p for p in partition(num, num) if len(p) <= slots]
    p = [z + [0] * (slots - len(z)) for z in p]  # pad
    return [y for z in p for y in unique_permutations(z)]


def notation(loops):
    qgraphs = graphgen()  # qgraf output
    graphs = []  # forcer output
    # read metagraph
    with open('metagraph{}.pickle'.format(loops), 'rb') as f:
        m = pickle.load(f)
        graphs = [x['graph'] for x in m.vs]

    #with open('notation.pickle', 'rb') as f:
    #    qgraphs = pickle.load(f)


    # part 1: generate all diagrams where the insertions are replaced by epsilons
    storage = []
    queue = [g for g in qgraphs]
    while len(queue) > 0:
        g = queue.pop()

        if all(not pathforcer.isIsomorphic(g, x)[0] for x in storage):
            print('new topo', ' '.join([str(e.tuple) + '*'*e['nonint'] for e in g.es]))
            storage.append(g)
        ins = [[ee.index for ee in g.es if ee.tuple == e] for e in set(g.get_edgelist()) if
            g.get_edgelist().count(e) > 1]

        for i in ins:
            # remove each insertion one by one
            h = g.copy()
            h.es[i[0]]['nonint'] += 1 + h.es[i[1]]['nonint']
            h.delete_edges([i[1]])

            # merge two point vertices
            while True:
                for v in range(h.vcount() - 1):
                    if len(h.neighbors(v)) == 2:
                        v1, v2 = h.neighbors(v)
                        # make sure the external line has the highest vertex
                        if len(h.neighbors(v2)) == 1:
                            v1, v2 = v2, v1

                        h.es[h.get_eid(v1, v)]['nonint'] += h.es[h.get_eid(v2, v)]['nonint']
                        h = pathforcer.removeEdge(h, h.get_eid(v2, v))
                        break
                else:
                    break

            queue.append(h)

    print('Number of topologies:', len(storage))
    qgraphs = storage

    # calculate all dependent momenta
    for g in qgraphs:
        if g.ecount() == 1:
            g['mom'] = []  # TODO: does this mean the topo is not included in the nezt step?
            continue

        # solve propagator system and find all ways to remove lines
        pp, _, dd_reps, _, _, _, _ = pathforcer.solvePropagatorSystem(g)

        # TODO: make sure that one of the outer lines is the representative of Q!
        ddmap = {}
        ddd = {}

        # make sure each propagator is in the map
        for i, x in enumerate(pp):
            if x not in dd_reps:
                dd_reps[x] = x

        for op, nps in sorted(dd_reps.items(), key = lambda k: pp.index(k[0])):
            if len(g.neighbors(g.es[pp.index(op)].target)) == 1:
                continue
            if nps in ddd:
                ddmap[pp.index(op)] = (1, ddd[nps])
            elif -nps in ddd:
                ddmap[pp.index(op)] = (-1, ddd[-nps])
            else:
                ddd[nps] = pp.index(op)

        dest = set(v[1] for v in ddmap.values())
        alloptions = [[x] + [k for k, v in ddmap.items() if v[1] == x] for x in dest]
        g['mom'] = alloptions
        g['momid'] = dict(list(ddmap.items()) + [(v[1],(v[0],k)) for k,v in ddmap.items()])


    # part 2: now for each dependent momentum group, we generate all possible epsilon distributions
    ngraphs = []
    for g in qgraphs:
        if len(g['mom']) == 0:
            ngraphs.append(g)
            continue

        epsilons = [sum(g.es[e]['nonint'] for e in groups) for groups in g['mom']]
        # generate all partitions
        dists = [distribution(e, len(group)) for group, e in zip(g['mom'], epsilons)]

        # product?
        for dist in product(*dists):
            for gr, d in zip(g['mom'], dist):
                h = g.copy()
                for e, ep in zip(gr, d):
                    h.es[e]['nonint'] = ep
                ngraphs.append(h)

    qgraphs = ngraphs
    print('Number of topologies:', len(qgraphs))

    with open('notation.pickle', 'wb') as f:
        pickle.dump(qgraphs, f, protocol=2)


    for g in graphs:
        g.es['dir'] = [1 for _ in range(g.ecount())]

    # part 3: filtering and matching
    # now we continue filtering out dependent momenta until we have a match
    ngraphs = []
    for g in qgraphs:
        if any(pathforcer.isIsomorphic(fg, g)[0] for fg in ngraphs):
            continue

        done = False
        for fg in graphs:
            if pathforcer.isIsomorphic(fg, g)[0]:
                ngraphs.append(fg)  # add forcer diagram
                done = True
                break
        if done:
            continue

        alloptions = g['mom']
        
        # now generate all joint options to remove lines
        options = [[(y, [z for z in x if z != y]) for y in x] for x in alloptions]

        for x in product(*options):
            removelines = sorted(((y,l) for l, xx in x for y in xx), key=lambda x:x[0], reverse=True)
            h = g.copy()

            removed = []
            for xx, l in removelines:
                removalmap = pathforcer.mapAfterRemoval(g.ecount(), removed)
                h.es[removalmap[l]]['nonint'] += h.es[removalmap[xx]]['nonint']
                h = pathforcer.removeEdge(h, removalmap[xx], preserveDirection=True)
                removed.append(xx)

            done = False
            for fg in graphs:
                iso = pathforcer.isIsomorphic(h, fg)
                if iso[0]:
                    # construct the filtering to obtain the forcer topology
                    # this involves mapping the line removal with sign over the isomorphism
                    done = True

                    # construct vertex map from g -> fg
                    vrmap = list(range(g.vcount()))
                    for e in removed:
                        vrmap = [v if v != vrmap[g.es[e].target] else vrmap[g.es[e].source] for v in vrmap]

                    vgfgmap = [iso[1][i] for i in vrmap]  # vertex map g -> fg                    

                    # now make edgemap from g -> fg
                    em = pathforcer.mapEdge(h, fg, iso[1], mapExt=False)
                    ermap = pathforcer.mapAfterRemoval(g.ecount(), removed)
                    gfgmap = [em[i] if i != len(ermap) else h.ecount() + removed.index(ii) for ii, i in enumerate(ermap)]

                    # transition g to fg
                    # all the new edges are at the back
                    gedgeids = [gfgmap.index(i) for i in range(g.ecount())]  # edge map fg -> g
                    gedgest = [g.es[i].tuple for i in gedgeids]
                    gdir = [1 for i in range(g.ecount())]

                    # find signs: if so, flip in new edge set
                    for ee,e in enumerate(gedgest[:g.ecount()]):
                        if vgfgmap[e[0]] > vgfgmap[e[1]]:
                            gdir[ee] = -1

                    # now tune the new edges so that they have the same sign as the line they are
                    # mapping to
                    removemap = []
                    for i, (removeedge, dest) in enumerate(removelines):
                        s, cur = 1, removeedge
                        while cur != dest:
                            s *= g['momid'][cur][0]
                            cur = g['momid'][cur][1]

                        # if the line is not changed, so change the dest
                        if (gdir[gedgeids.index(dest)] == 1 and s == -1) or \
                            (gdir[gedgeids.index(dest)] == -1 and s == 1):
                            gdir[fg.ecount() + i] = -1

                        removemap.append((fg.ecount() + i, gedgeids.index(dest)))

                    gg = Graph(n=g.vcount())
                    gg.add_edges(gedgest)
                    gg['removeedges'] = removemap
                    gg.es['nonint'] = [g.es[i]['nonint'] for i in gedgeids]
                    gg.es['dir'] = gdir
                    gg['dots'] = []
                    gg['name'] = fg['name']

                    ngraphs.append(gg)
                    break
            if done:
                break
        else:
            print('Could not match topo', ' '.join([str(e.tuple) + '*'*e['nonint'] for e in g.es]), options, ddmap)

    qgraphs = ngraphs
    print('Number of topologies:', len(qgraphs))

    isext = lambda e,g : len(g.neighbors(e.source)) == 1 or len(g.neighbors(e.target)) == 1

    with open('notation.h', 'w') as f:
        gs = []
        for g in qgraphs:
            cgi = pathforcer.mapAfterRemoval(g.ecount(), [e.index for e in g.es if isext(e, g)])

            if g.ecount() == 1:
                continue
            vs = []
            for v in g.vs:
                if len(v.neighbors()) < 2:
                    continue
                s = []
                for e in g.es:
                    if v.index in e.tuple:
                        #n = e.tuple[0] if v.index == e.tuple[1] else e.tuple[1]
                        d = (1 if e.tuple[0] == v.index else -1) * e['dir']
                        ss = '' if d == 1 else '-'
                        ss += 'Q' if isext(e, g) else 'p' + str(cgi[e.index] + 1)
                        s.append(ss)
                vs.append('vx({})'.format(','.join(s)))

            es = ','.join('Q' if isext(e, g) else 'p' + str(cgi[e.index] + 1) for e in g.es for k in range(e['nonint']))

            # generate automorphisms for the case without dot products
            olddots = [y for y in g['dots']]
            g['dots'] = []
            autos = ''
            auto = pathforcer.getAutomorphisms(g)
            g['dots'] = olddots

            if len(auto) > 0:
                em = []
                for vmap, emaps in auto[0][1].items():
                    for emap in emaps:
                        em.append(
                            next('Q,-Q,' if vmap[e.source] > vmap[e.target] else '' for e in g.es if isext(e, g)) +
                            ','.join('p{0},{1}p{2}'.format(
                                i + 1,
                            '-'*((vmap[g.es[cgi.index(i)].source] - vmap[g.es[cgi.index(i)].target]) *
                            g.es[cgi.index(i)]['dir'] * g.es[emap[cgi.index(i)]]['dir'] > 0), # sign
                            cgi[emap[cgi.index(i)]] + 1) for i in range(g.ecount() - 2) if 
                                cgi[emap[cgi.index(i)]] != i or 
                                (vmap[g.es[cgi.index(i)].source] - vmap[g.es[cgi.index(i)].target]) *
                            g.es[cgi.index(i)]['dir'] * g.es[emap[cgi.index(i)]]['dir'] > 0))

                rules = tuple('*SYM({})'.format(e) for e in em)  # filter double rules
                autos = '\n ' + '\n '.join(''.join(rules[i:i+1]) for i in range(0, len(rules), 1))

            removeedges = ''
            if 'removeedges' in g.attributes():
                removeedges = '\n *ID({})'.format(','.join('p{},{}'.format(cgi[a[0]] + 1,
                    'Q' if isext(g.es[a[1]], g) else 'p' + str(cgi[a[1]] + 1)) for a in g['removeedges']))

            gs.append('{}{}{}{}\n *TOPO(M{})'.format('\n *'.join(vs), '\n *ex({})'.format(es)*(len(es) > 0), autos, 
                removeedges, g['name']))
        f.write("\n+".join(gs) + "\n")

if __name__ == '__main__':
    parser = argparse.ArgumentParser()
    parser.add_argument('--loops', nargs=1, default=4, type=int, help='Number of loops')
    args = parser.parse_args()
    notation(args.loops)
